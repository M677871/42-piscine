#include <unistd.h>

void	print_w(char *w)
{
	static int	f;
	char		c;

	if (!f)
		write(1, " ", 1);
	f = 0;
	c = *w++;
	while (c != '\0')
	{
		c = *w++;
		write(1, &c, 1);
	}
}
